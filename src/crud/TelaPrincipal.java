    public void readTable() {

        DefaultTableModel modelo = (DefaultTableModel) tbl_view.getModel();
        modelo.setNumRows(0);
        ProdutoDAO dao = new ProdutoDAO();

        for (Produto p : dao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getFornecedor(),
                p.getPrecoOrig(),
                p.getPrecoFin(),
                p.getQnt(),
                p.getLimite()

            });

        }

    }
    public void readTableFind(String pFind) {

        DefaultTableModel modelo = (DefaultTableModel) tbl_view.getModel();
        modelo.setNumRows(0);
        ProdutoDAO dao = new ProdutoDAO();

        for (Produto p : dao.readLike(pFind)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getFornecedor(),
                p.getPrecoOrig(),
                p.getPrecoFin(),
                p.getQnt(),
                p.getLimite()

            });

        }

    }


