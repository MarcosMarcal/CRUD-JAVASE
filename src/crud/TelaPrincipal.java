    private void btn_attActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_attActionPerformed
        // TODO add your handling code here:
        if (tbl_view.getSelectedRow() != -1) {

            Produto p = new Produto();
            p.setId(Integer.parseInt(tbl_view.getValueAt(tbl_view.getSelectedRow(), 0).toString()));
            p.setDescricao(tbl_view.getValueAt(tbl_view.getSelectedRow(), 1).toString());
            p.setFornecedor(tbl_view.getValueAt(tbl_view.getSelectedRow(), 2).toString());
            p.setPrecoOrig(Float.parseFloat(tbl_view.getValueAt(tbl_view.getSelectedRow(), 3).toString()));
            p.setPrecoFin(Float.parseFloat(tbl_view.getValueAt(tbl_view.getSelectedRow(), 4).toString()));
            p.setQnt(Integer.parseInt(tbl_view.getValueAt(tbl_view.getSelectedRow(), 5).toString()));
            p.setLimite(Integer.parseInt(tbl_view.getValueAt(tbl_view.getSelectedRow(), 6).toString()));

            new TelaCadastro(p).setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos um produto!");
        }


    }//GEN-LAST:event_btn_attActionPerformed

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        // TODO add your handling code here:
        if (tbl_view.getSelectedRow() != -1) {

            if (JOptionPane.showConfirmDialog(null, "Deseja realmente apagar esse registro?", "Apagar produto selecionado", JOptionPane.YES_NO_CANCEL_OPTION) == 0) {
                //JOptionPane.showMessageDialog(null, "clicou sim");
                Produto p = new Produto();
                ProdutoDAO dao = new ProdutoDAO();
                p.setId(Integer.parseInt(tbl_view.getValueAt(tbl_view.getSelectedRow(), 0).toString()));
                dao.delete(p);
                
            } else {
                JOptionPane.showMessageDialog(null, "Nada foi apagado!");

            }

        }
    }//GEN-LAST:event_btn_delActionPerformed

